# STRING FUNCTIONS 

myStr = 'HelloWorld'

# Capitalize 
print(myStr.capitalize())

# Swap case 
print(myStr.swapcase())

# Get the length
print(len(myStr))

# Replace 
print(myStr.replace('World', 'Everyone'))

# Count 
print(myStr.count("o"))

# Startswith
print(myStr.startswith("Hell"))

# Endswith
print(myStr.endswith("!"))

# Split to list 
print(myStr.split())

# find
print(myStr.find('e'))

# index
print(myStr.index('e'))

# Is all alphanumeric
print(myStr.isalnum())

# Is alphabetic
print(myStr.isalpha())

# Is all numeric
print(myStr.isnumeric())




