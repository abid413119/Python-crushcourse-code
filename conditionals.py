# CONDITIONALS

x = 4

# Basic if 

if x < 6 :
    print('this is true')

# If Else 

if x < 5 : 
    print("This is true")
else: 
    print('This is false')

# Elif

color = 'red'

if color == 'red':
    print('Color is red.')
elif color == 'blue':
    print('Color is blue')
else:
    print('Color is not red or blue')

# Logical operator
if color == 'red' and x < 10:
    print('Color is red and x is less than 10')

